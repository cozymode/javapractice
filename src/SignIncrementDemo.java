public class SignIncrementDemo {

  public static void main(String[] args) {

    int i = 5;
    int j = 5;
    int x = 5;
    int y = 5;

    int res = i++ * x; //
    int res2 = ++j * y;

    System.out.println(res);
    System.out.println(res2);
    System.out.println(i);
    System.out.println(j);

  }

}
